import type { PublicAnalysisResponse } from "@/lib/agents/types";

const scenarioToRequest: Record<string, string[]> = {
  "拖欠工资": ["请求被申请人支付拖欠的工资共计____元"],
  "违法解除/辞退": [
    "请求确认被申请人解除劳动关系的行为违法",
    "请求被申请人支付违法解除劳动合同赔偿金____元（2N）",
  ],
  "未签书面劳动合同": ["请求被申请人支付未签订书面劳动合同的双倍工资差额____元"],
  "加班费/工时争议": ["请求被申请人支付加班工资____元"],
  "社会保险争议": ["请求被申请人补缴____年__月至____年__月的社会保险"],
  "工伤待遇争议": [
    "请求被申请人支付停工留薪期工资____元",
    "请求被申请人支付一次性伤残补助金____元",
  ],
  "女职工特殊保护": [
    "请求确认被申请人解除劳动关系的行为违法",
    "请求被申请人支付违法解除劳动合同赔偿金____元",
  ],
  "工资福利/休假争议": ["请求被申请人支付____费用共计____元"],
};

export function generateArbitrationDoc(result: PublicAnalysisResponse): string {
  const now = new Date();
  const dateStr = `${now.getFullYear()}年${now.getMonth() + 1}月${now.getDate()}日`;

  const requests = scenarioToRequest[result.scenarioLabel] || [
    "请求被申请人支付____元",
  ];

  return `劳动仲裁申请书

申请人：____（姓名）
性别：____ 民族：____ 出生日期：____年__月__日
身份证号码：____________________
住址：____________________________
联系电话：____________________

被申请人：____（公司全称）
法定代表人：____
地址：____________________________
联系电话：____________________

仲裁请求：

${requests.map((r, i) => `${i + 1}. ${r}；`).join("\n")}

事实与理由：

${result.answer}

${result.compensationRange ? `【赔偿参考】${result.compensationRange}` : ""}

证据清单：

1. ____（如：劳动合同/工资银行流水/聊天记录截图）
2. ____
3. ____
4. ____

（提示：${result.nextSteps.slice(0, 2).join("；")}）

此致
重庆市____区劳动人事争议仲裁委员会

申请人（签名）：____________

日期：${dateStr}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
注意事项：
- 请将"____"处替换为你的实际信息
- 仲裁请求中的金额需要你自己计算填写
- 建议打印一式三份（自留一份、交仲裁委两份）
- 如需帮助，拨打 12348 法律援助热线（免费）
- 本文档由AI辅助生成，仅供参考
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`;
}
